function y = dmyPoly(x)
% Function Name: dmyPoly
%
% Description: Derivative of the polynom in myPoly.
%
% Syntax:  y = dmyPoly(x)
%
% Inputs: x - Location to calculate the analytical derivative of myPoli at.
%
% Example: 
%    y = dmyPoly(3)
%
% Other m-files required: none
% Subfunctions: none
% MAT-files required: none
%
% See also: myNewton, myPoly
%
% Author: Lukas Benner
% Date: March 30, 2022

% ------------- BEGIN CODE --------------

y = 2*(x-3);
end